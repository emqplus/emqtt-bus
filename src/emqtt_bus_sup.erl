-module(emqtt_bus_sup).

-behaviour(supervisor).

-export([start_link/0]).

-export([init/1]).

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

init([]) ->
    Bus = #{id       => emqtt_bus,
			start    => {emqtt_bus, start_link, [0]},
            restart  => permanent,
            shutdown => 5000,
            type     => worker,
            modules  => [emqtt_bus]
		   },
    {ok, {{one_for_one, 100, 3600}, [Bus]}}.
